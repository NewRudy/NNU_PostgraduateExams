/*    C 语言基本数据类型的声明          */
/* ======================================== */

int main()
{
   int    i = 2;                       /* 整数声明       */
   char   c = 'c';                     /* 字符声明       */
   float  f = 1.234;                   /* 单精度浮点数声明 */
   double d = 2.345;                   /* 双精度浮点数声明*/

   printf("输出变量值:%d %c %f %f\n",  /* 输出变量值     */
           i,c,f,d);

   return 0;
}
