/*    C 语言数组结构的声明和输出            */
/* ======================================== */

int main()
{
   int    i[5] = { 1,2,3,4,5 };
   char   c[5] = { 'c','d','e','f','g' };
   float  f[5] = { 1.2,1.23,1.234,1.2345,1.23456 };
   double d[5] = { 2.34567,2.3456,2.345,2.34,2.3 };
   int    j;

   for ( j = 0; j < 5; j++ )             /* 循环       */
      printf("输出变量值:%d %c %f %f\n", /* 输出变量值 */
              i[j],c[j],f[j],d[j]);

   return 0;
}
